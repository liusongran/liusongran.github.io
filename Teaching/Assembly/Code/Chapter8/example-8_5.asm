SSEG	SEGMENT
SKTOP	DB		20H DUP (0)
SSEG	ENDS

CSEG	SEGMENT
		ASSUME  CS:CSEG
FACT	PROC	NEAR
		AND		BX, BX
		JZ		FACT1
		PUSH    BX
		DEC		BX
		CALL   	FACT
F:		POP		BX
		MUL     BX
		RET
FACT1: 	MOV     AX,1
		RET
FACT    ENDP

MAIN:	MOV		AX, SSEG
		MOV		DS, AX
		MOV		SP, SIZE SKTOP
		MOV		BX, 4
		CALL	FACT
		NOP
		MOV  	AH, 4CH
		INT  	21H
CSEG	ENDS
END		MAIN

